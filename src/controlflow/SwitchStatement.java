package controlflow;

public class SwitchStatement {
    public static void main(String[] args) {
        int a = 5 ;

        switch (a) {
            case 1:
                System.out.println(a +"a is equal to 1");
                break;
            case 2:
                System.out.println(a +"a is equal to 2");
                break;
            case 3:
                System.out.println(a +"a is equal to 3");
                break;
            case 4:
                System.out.println(a +"a is equal to 4");
                break;
            case 5:
                System.out.println(a +"a is equal to 5");
                break;
            case 6:
                System.out.println(a +"a is equal to 6");
                break;
        }
    }

}
